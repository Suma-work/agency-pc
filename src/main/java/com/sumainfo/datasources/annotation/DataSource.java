package com.sumainfo.datasources.annotation;

import java.lang.annotation.*;

/**
 * 多数据源注解
* @Title: DataSource.java 
* @Package com.sumainfo.datasources.annotation  
* @author zhlu
* @date 2018年3月15日
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
@Documented
public @interface DataSource {
    String name() default "";
}
